/**
 * Density-LOD γραμμοσκίασης — **«πότε το μοτίβο είναι πολύ πυκνό για να διαβαστεί;»** (SSoT).
 *
 * **ΕΝΑ ερώτημα, ΔΥΟ κλίμακες.** Η οθόνη ρωτά «πόσα **px** απέχουν οι γραμμές στο τρέχον zoom;»,
 * το χαρτί ρωτά «πόσα **mm** απέχουν στην τρέχουσα κλίμακα εκτύπωσης;». Είναι **η ίδια ερώτηση**
 * με άλλο μέτρο ⇒ **ένας** πυρήνας ({@link hatchMinWorldSpacing} × κλίμακα < κατώφλι), δύο
 * ονομασμένα περιτυλίγματα που κλειδώνουν το **ζευγάρι μονάδων**.
 *
 * ## 🔴 Γιατί υπάρχει αυτό το αρχείο (ADR-667 Φ3.1 — μετρημένο περιστατικό, όχι θεωρία)
 *
 * Οι σταθερές ήταν **module-private** στον `HatchRenderer` και ο έλεγχος **μέθοδος** του. Άρα το
 * vector PDF export **δεν μπορούσε να τον δει** — και η **Απόφαση 7** του ADR-667 τον απέρριψε
 * ρητά με το σκεπτικό *«είναι συνάρτηση του zoom· το χαρτί δεν έχει zoom»*. **Αυτό ήταν λάθος και
 * το απέδειξε πραγματικό PDF:** γραμμοσκίαση «Διαγώνιες 45°» βγήκε **συμπαγής μαύρη μάζα** ενώ ο
 * καμβάς την έδειχνε σωστά. Μέτρηση στο εξαγόμενο αρχείο:
 *
 * | Μέγεθος | Τιμή |
 * |---|---|
 * | Απόσταση διαδοχικών διαγωνίων στο **χαρτί** | **0,089 mm** (p90: 0,168 mm) |
 * | Πάχος γραμμής μοτίβου | **0,18 mm** |
 * | Λόγος πάχος ÷ απόσταση | **≈ 2,0** ⇒ **~200% κάλυψη μελανιού** |
 *
 * Οι γραμμές ήταν **διπλάσια χοντρές από το κενό μεταξύ τους** ⇒ **μαθηματικά εγγυημένο μαύρο**.
 * **Το χαρτί ΕΧΕΙ κλίμακα** (`worldToPaperScale`, mm ανά μονάδα σχεδίου) — είναι το ακριβές
 * ανάλογο του `transform.scale`. Το «δεν έχει zoom» ήταν σοφιστεία.
 *
 * ⚠️ **Ο budget guard ΔΕΝ μπορεί να πάρει τη θέση αυτού του ελέγχου** (το πίστευε η Απόφαση 7):
 * μετράει **ΠΛΗΘΟΣ** (40k/120k), όχι **ΑΝΑΓΝΩΣΙΜΟΤΗΤΑ**. Οι 69.136 γραμμές του περιστατικού
 * πέρασαν **άνετα**. 500 γραμμές που πέφτουν η μία πάνω στην άλλη περνούν κι αυτές — και βγάζουν
 * μαύρο πλακάκι. **Απαντούν σε άλλο ερώτημα** ⇒ ο budget guard **μένει**, αυτό είναι **επιπλέον**.
 *
 * ## 🏛️ Η πρακτική των μεγάλων παιχτών (ερευνήθηκε — δική τους πρακτική, όχι δική μας εφεύρεση)
 *
 * - **Revit** — *pattern overscaling*, **επίσημα τεκμηριωμένο**: *«Καθώς κάνετε zoom out, το μοτίβο
 *   γίνεται πυκνότερο. Σε κάποιο σημείο, εμφανίζεται ως συμπαγές γέμισμα.»* Ο μηχανισμός είναι
 *   **resolution-driven** (κάτω από ~1 pixel εξόδου) ⇒ **ισχύει και στην εκτύπωση**. Είναι ο
 *   **μόνος** από τους τέσσερις που λύνει το πρόβλημα — και είναι **ακριβώς ό,τι κάνει ήδη η
 *   οθόνη μας**. Το κάνει **σιωπηλά**· εμείς όχι (ADR-667 Απόφαση 11).
 * - **AutoCAD** — μπλοκάρει στη **δημιουργία** (`HPMAXLINES` → *«Hatch spacing too dense, or dash
 *   size too small»*), αλλά στο **plot** τυπώνει **κάθε γραμμή** ⇒ μαύρη μάζα + φουσκωμένο PDF.
 *   Δηλαδή **η σημερινή μας συμπεριφορά ήταν η συμπεριφορά του AutoCAD** — το πρόβλημα, όχι η λύση.
 *   ⚠️ Το παλιό σχόλιο εδώ επικαλούνταν το AutoCAD ως πηγή αυτού του LOD· **ήταν λάθος**.
 * - **ArchiCAD / MicroStation** — **καμία απόδειξη** ότι κάνουν οτιδήποτε (αναφέρεται ως «δεν
 *   βρέθηκε», όχι ως «κάνουν το ίδιο»).
 *
 * ## 📐 Το κατώφλι δεν είναι δικό μας νούμερο — είναι **προτύπου**
 *
 * **ISO 128-2:** *η ελάχιστη απόσταση μεταξύ παράλληλων γραμμών δεν πρέπει να είναι μικρότερη από
 * **δύο φορές** το πάχος της χονδρότερης γραμμής, και **ποτέ κάτω από 0,7 mm**.* Το
 * {@link HATCH_MIN_LINE_SPACING_PAPER_MM} (0,8 mm) είναι **πάνω** από αυτό το δάπεδο ⇒ κάτω από
 * αυτό, το μοτίβο **δεν είναι αναγνώσιμο σε καμία εκτύπωση, κατά το πρότυπο**. (Τα 0,089 mm του
 * περιστατικού είναι **8× κάτω** από το όριο του ISO.)
 *
 * @module subapps/dxf-viewer/rendering/entities/shared/hatch-density-lod
 * @see docs/centralized-systems/reference/adrs/ADR-667-pdf-native-tiling-patterns.md — Απόφαση 7
 * @see bim/geometry/shared/hatch-pattern-geometry.ts — `hatchMinWorldSpacing`, η μισή μέτρηση
 */

import { hatchMinWorldSpacing } from '../../../bim/geometry/shared/hatch-pattern-geometry';

/** Ό,τι χρειάζεται ο έλεγχος — το ίδιο `Pick<>` που απαιτεί το {@link hatchMinWorldSpacing}. */
type HatchDensityProbe = Parameters<typeof hatchMinWorldSpacing>[0];

/**
 * Κατώφλι **οθόνης**: κάτω από αυτή την on-screen απόσταση (px) οι γραμμές μοτίβου γίνονται
 * **δυσδιάκριτη μάζα** → tint. Παραλείπει **και** την παραγωγή segments ⇒ μηδέν cost σε zoom-out
 * (η κύρια αιτία βαρύτητας σε πυκνά σχέδια).
 */
export const HATCH_MIN_LINE_SPACING_PX = 3;

/**
 * Κατώφλι **χαρτιού** (mm) — το κάτοπτρο του {@link HATCH_MIN_LINE_SPACING_PX}.
 *
 * **Δύο ανεξάρτητες αιτιολογήσεις συγκλίνουν στο ίδιο νούμερο:**
 * 1. **Αντιστοιχία οθόνης:** 3 px @ 96 dpi ≈ 0,79 mm ⇒ **ίδια οπτική πυκνότητα** με αυτή που
 *    βλέπει ο χρήστης στο 100% zoom (preview ≡ commit, ADR-667 Απόφαση 12).
 * 2. **ISO 128-2:** απόλυτο δάπεδο **0,7 mm** μεταξύ παράλληλων γραμμών. Το 0,8 το σέβεται.
 *
 * 🚫 **ΔΕΝ είναι το `SCREEN_HATCH_PAPER_SPACING_MM`, παρότι έχει την ΙΔΙΑ τιμή.** Εκείνο απαντά
 * «σε **πόση** απόσταση **ζωγραφίζουμε** τις ρίγες του Τέκτονα στο χαρτί»· αυτό απαντά «κάτω από
 * **πόση** απόσταση **αρνούμαστε** να ζωγραφίσουμε γραμμές». **Άλλο ερώτημα, συμπτωματικά ίδιος
 * αριθμός** (και τα δύο πηγάζουν από το `3 px @ 96 dpi`, αλλά από **διαφορετικά** 3άρια). Αν
 * κάποιος αλλάξει την πυκνότητα των ριγών, αυτό το κατώφλι **δεν πρέπει** να κουνηθεί μαζί.
 */
export const HATCH_MIN_LINE_SPACING_PAPER_MM = 0.8;

/**
 * Διαφάνεια του collapsed solid tint. Διαβάζεται ως «γεμάτο» χωρίς να βαραίνει (οθόνη) και χωρίς
 * να γίνεται μαύρο πλακάκι (χαρτί) — το ανάλογο του «lighten» του Revit overscaling.
 */
export const HATCH_COLLAPSE_ALPHA = 0.45;

/**
 * Ο **πυρήνας**: η πυκνότητα του μοτίβου, προβεβλημένη στο μέτρο του μέσου.
 *
 * `scale` και `minSpacing` πρέπει να είναι στο **ίδιο ζευγάρι μονάδων** — γι' αυτό η συνάρτηση
 * είναι private και εκτίθεται **μόνο** μέσω των δύο περιτυλιγμάτων (ένα λάθος ζευγάρωμα εδώ
 * βγάζει σιωπηλά λάθος PDF).
 */
function isHatchDensityTooHigh(
  hatch: HatchDensityProbe, scale: number, minSpacing: number,
): boolean {
  const worldSpacing = hatchMinWorldSpacing(hatch);
  // 0 = solid (καμία γραμμή) ⇒ τίποτα να καταρρεύσει. Αρνητικό/NaN ⇒ μη μετρήσιμο, μην πειράζεις.
  if (!(worldSpacing > 0)) return false;
  return worldSpacing * scale < minSpacing;
}

/**
 * True όταν οι γραμμές μοτίβου είναι sub-pixel πυκνές στο **τρέχον zoom** → LOD tint.
 * @param pxPerWorldUnit `transform.scale` του renderer.
 */
export function isHatchDensityTooHighOnScreen(
  hatch: HatchDensityProbe, pxPerWorldUnit: number,
): boolean {
  return isHatchDensityTooHigh(hatch, pxPerWorldUnit, HATCH_MIN_LINE_SPACING_PX);
}

/**
 * True όταν οι γραμμές μοτίβου είναι **κάτω από το όριο αναγνωσιμότητας του χαρτιού** (ISO 128-2)
 * στην **τρέχουσα κλίμακα εκτύπωσης** → tint αντί για μαύρη μάζα (Revit *pattern overscaling*).
 * @param mmPerWorldUnit `worldToPaperScale` (`capture-2d-vector.ts` — **ήδη υπολογισμένο**).
 */
export function isHatchDensityTooHighOnPaper(
  hatch: HatchDensityProbe, mmPerWorldUnit: number,
): boolean {
  return isHatchDensityTooHigh(hatch, mmPerWorldUnit, HATCH_MIN_LINE_SPACING_PAPER_MM);
}
