/**
 * ADR-651 Φάση Η — **AI structured-output schema** για την αυτόματη περιγραφή αναθεώρησης
 * (§8 #6 «auto-changelog»· Απόφαση #9: «προτείνει μόνο του τι άλλαξε»).
 *
 * Το μοντέλο **δεν** μετράει και **δεν** ερμηνεύει σχέδιο: του δίνουμε τον **ήδη
 * υπολογισμένο, ντετερμινιστικό diff** (`revision-diff.ts`) και του ζητάμε ΜΟΝΟ να τον
 * διατυπώσει σε επαγγελματικά ελληνικά/αγγλικά. Έτσι τα νούμερα είναι πάντα σωστά — το LLM
 * κάνει μόνο αυτό που ξέρει να κάνει καλά (γλώσσα), όχι αρίθμηση.
 *
 * Strict-mode σύμβαση (ίδια με Φάση Δ): όλα υποχρεωτικά, τα προαιρετικά `.nullable()`.
 */

import { z } from 'zod';

export const aiRevisionChangelogSchema = z.object({
  /**
   * Η **πρόταση** περιγραφής (1-2 προτάσεις) — μπαίνει στο `{{revision.description}}`.
   * ΠΟΤΕ δεν γράφεται αυτόματα: ο χρήστης την εγκρίνει ή τη διορθώνει (Απόφαση #9).
   */
  description: z.string(),
  /** Σύντομα σημεία ανά φύλλο («Κάτοψη Ισογείου: +3 τοίχοι, 1 άνοιγμα καταργήθηκε»). */
  highlights: z.array(z.string()),
  /** Πόσο σίγουρο είναι το μοντέλο ότι η περιγραφή αποδίδει τον diff (0-1). */
  confidence: z.number().min(0).max(1),
  /** Επιφύλαξη (π.χ. «χονδρική σύγκριση — δεν φαίνονται επί τόπου τροποποιήσεις»). */
  notes: z.string().nullable(),
});

export type AiRevisionChangelog = z.infer<typeof aiRevisionChangelogSchema>;
